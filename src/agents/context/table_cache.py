"""
Table Summary Cache
Responsible for caching table summaries generated by the table_summarizer tool, 
persisting to a JSON file to avoid repeated LLM calls.
"""
import os
import json
from typing import Dict, Optional
from dataclasses import dataclass, asdict


@dataclass
class TableSummary:
    """Table summary information"""
    file_path: str          # Normalized absolute path
    last_modified: float    # File last modified timestamp
    summary: Dict           # Summary data


class TableCache:
    """
    Table Summary Cache Manager
    - Persists to JSON file
    - Indexed based on true file path
    - Automatically detects file modifications
    """
    
    def __init__(self, cache_file: str = "data/table_summary_cache.json"):
        """
        Args:
            cache_file: Path to the cache file
        """
        self.cache_file = cache_file
        self._cache: Dict[str, TableSummary] = {}
        self._load()
    
    def _normalize_path(self, file_path: str) -> str:
        """Normalize file path to absolute path"""
        return os.path.realpath(os.path.expanduser(file_path))
    
    def get(self, file_path: str) -> Optional[Dict]:
        """
        Get table summary, automatically detecting if the file has been modified.
        
        Args:
            file_path: File path (relative or absolute)
            
        Returns:
            Summary data, returns None if cache is invalid or does not exist.
        """
        if not os.path.isfile(file_path):
            return None
        
        norm_path = self._normalize_path(file_path)
        
        if norm_path in self._cache:
            cached = self._cache[norm_path]
            current_mtime = os.path.getmtime(norm_path)
            
            # Check if file has been modified
            if cached.last_modified >= current_mtime:
                return cached.summary
            else:
                # File modified, delete expired cache
                del self._cache[norm_path]
        
        return None
    
    def set(self, file_path: str, summary: Dict):
        """
        Cache table summary and save to disk.
        
        Args:
            file_path: File path
            summary: Summary data
        """
        norm_path = self._normalize_path(file_path)
        mtime = os.path.getmtime(norm_path)
        
        self._cache[norm_path] = TableSummary(
            file_path=norm_path,
            last_modified=mtime,
            summary=summary
        )
        
        self._save()
    
    def _load(self):
        """Load cache from disk"""
        if not os.path.exists(self.cache_file):
            return
        
        try:
            with open(self.cache_file, 'r', encoding='utf-8') as f:
                data = json.load(f)
                for path, item in data.items():
                    self._cache[path] = TableSummary(
                        file_path=item['file_path'],
                        last_modified=item['last_modified'],
                        summary=item['summary']
                    )
        except Exception as e:
            print(f"[TableCache] Failed to load cache: {e}")
    
    def _save(self):
        """Save cache to disk"""
        os.makedirs(os.path.dirname(self.cache_file), exist_ok=True)
        
        try:
            data = {
                path: asdict(summary) 
                for path, summary in self._cache.items()
            }
            with open(self.cache_file, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
        except Exception as e:
            print(f"[TableCache] Failed to save cache: {e}")
    
    def clear(self):
        """Clear all cache"""
        self._cache.clear()
        self._save()
